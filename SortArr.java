// sorting will be in ascending order (chote se bada)
import java.util.Arrays;
public class SortArr {
    public static void main(String args[]){
        int marks[] = new int[5];
        marks[0]=100;
        marks[1]=99;
        marks[2]=98;
        marks[3]=97;
        marks[4]=96;
        Arrays.sort(marks);
        System.out.println(marks[0]);
    }
    
}
