
public class ConcatStr {
    public static void main(String[] args) { //This function joins strings
        String s1 = "AKARSH";
        String s2 = "JHA";
        String s3 = s1.concat(s2);
        String s4 = s1 + " " + s2;
        System.out.println(s3);
        System.out.println(s4);
    }
}
